#Assignment Ooperator
## =, +=, -=, /=, **=, %=, //=, --, ++

number = 120

# +=
number+=40 # number=number + 40
print(number)

# -=
number-=60 #number=number - 60
print(number)


# *=
number*=2 # number = number * 2
print(f"Multiply and Assign: {number}")

# /=
y = 550
y/=2 # y = y / 2
print(y)

# **=
y**=3 # y = y ** 3
print(y)

# %=
y%=3 #y = y % 3
print(y)

#//=
y//=3 # y = y // 3
print(y)

# ++
#Increment Operator
x = 50
x += 1 # x = x+ 1
print(x)

#--
# Decrement Operator
y = 3
y-=1 # y = y+ 1
print(y)